   <h1 align="center">Game Boy</h1>
    <hr>
<b>Game Boy</b> — линейка портативных игровых устройств, разработанная и производившаяся Nintendo.
<p><img src="images/nit1.png" alt="nintendo" width="30%" height="30%" class="leftimg">


 <br> <br>
<h1 align="center">Обзор</h1>
 <hr>
Первый Game Boy был выпущен ещё в 1989 году, как продолжение к известным карманным играм Nintendo Game & Watch. Это была первая популярная портативная игровая система, и многие компании пытались повторить успех Game Boy, но никому сделать этого так и не удалось.

<br><br>Устройство имело монохромный жидкокристаллический экран с размерами 160?144 пикселей, стереозвук на четыре канала и функцию GameLink, позволяющую соединять две консоли вместе и играть вдвоём в поддерживающие данную технологию игры.

<br><br>Всякий раз, если по каким-либо причинам продажи Game Boy шли на убыль, Nintendo выпускала новые разновидности Game Boy, чтобы снова увеличить продажи. Таким образом, в ноябре 1996 года появился Game Boy Pocket, который был меньше в размерах на 30 %, чем свой предшественник, но с экраном большего размера и всего с двумя батареями AAA. Позже был выпущен Game Boy Color c цветным экраном, затем — Game Boy Advance.
