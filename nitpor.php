<h1 align="center">Технические характеристики</h1>
 <hr>
<ul>Процессор:</ul> 
<li>Sharp LR35902 DMC-CPU[2], 8-разрядный (сильно упрощённый вариант Zilog Z80[3] производства Sharp), на тактовой частоте 4,194304 МГц</li>
<ul>Память:</ul>
<li>Основная память: 8 Кбайт</li>
<li>Видеопамять: 8 Кбайт</li>
<li>Размер экрана: 2,6" (66 мм)</li>
<li>Разрешение: 160x144 пикселей</li>
<li>Максимальное число спрайтов: 40, но не более 10 в одной строке</li>
<li>Размеры спрайтов: 8x16 либо 8x8 пикселей</li>
<li>Горизонтальная развертка: 9198 кГц</li>
<li>Вертикальная развертка, 59,73 Гц</li>
<li>Звук: 4-канальный, стерео</li>
<li>Питание: DC6V 0,7 Вт — 4 элемента типа AA</li>
<li>Размеры: 148x90x32 мм
Вес (с элементами питания): 394 г
Мин/макс ёмкость картриджа: 256 кбит — 4 Мбит (32-500 Кб)</li>
